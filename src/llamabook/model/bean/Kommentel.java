package llamabook.model.bean;


public class Kommentel {

	private String email;
	private int posztID;
	private String komment;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getPosztID() {
		return posztID;
	}

	public void setPosztID(int posztID) {
		this.posztID = posztID;
	}

	public String getKomment() {
		return komment;
	}

	public void setKomment(String komment) {
		this.komment = komment;
	}




}
